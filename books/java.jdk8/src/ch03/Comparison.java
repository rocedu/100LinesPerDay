package ch03;

public class Comparison {
    public static void main(String[] args) {
        System.out.printf("10 >  5 结果 %b%n", 10 > 5);
        System.out.printf("10 >= 5 结果 %b%n", 10 >= 5);
        System.out.printf("10 <  5 结果 %b%n", 10 < 5);
        System.out.printf("10 <= 5 结果 %b%n", 10 <= 5);
        System.out.printf("10 == 5 结果 %b%n", 10 == 5);
        System.out.printf("10 != 5 结果 %b%n", 10 != 5);
    }
}
